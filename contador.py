# Inicializa a variável contador com o valor 1
contador = 1

# Inicia um loop while que continua enquanto contador for menor ou igual a 10
while(contador <= 10):
    # Imprime o valor atual de contador
    print(contador)
    
    # Incrementa contador em 2
    contador = contador + 2
    
    # Verifica se o valor de contador é igual a 5
    if(contador == 5):
        # Se contador for igual a 5, incrementa contador em 2 adicionais
        contador = contador + 2
